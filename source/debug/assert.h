// ***************************************************************************
// assert - runtime assertion checking
// ***************************************************************************

#pragma once
#include "debug.h"

// ---------------------------------------------------------------------------
// assert(expr);
// ---------------------------------------------------------------------------

#ifndef NDEBUG
#define assert(expr) \
	if (!(expr)) \
		_f_debug("ASSERT FAILED!\x80", 0, 0l, 0l, __FILE__, sizeof(__FILE__), __LINE__);
#else
#define assert(expr)
#endif

